/**
 * 接口视角约束 —— 供"文档/主干链路 → 接口功能用例"两个生成 prompt 共用。
 *
 * 背景：需求/业务流程文档常以"前端/用户视角"书写（点按钮、看页面、提示文案），
 * AI 容易照抄成前端味的步骤与断言。但我们要的是**接口契约视角**的测试设计——
 * 断言只断接口响应能验证的事实，纯 UI 动作不作为接口测试步骤。否则下游生成的
 * 待编排用例会"断在页面元素上"，执行必失败或被 AI 擅自改写，属"看起来在跑实则测错"。
 */
export const INTERFACE_PERSPECTIVE_BLOCK = `
## 接口视角（最重要的口径，务必遵守）

输入文档可能是**前端/用户视角**写的（"点击提交""页面显示""提示成功"）。
你要把它**翻译成接口契约视角**，而不是照抄前端措辞。这关系到下游能否生成可执行的接口用例。

- **步骤 action**：写"调用了什么业务能力/接口动作"，不是"用户点了什么"。
- **断言 / 后置 / 预期**：只写"**接口响应里能验证的事实**"——返回码、返回字段的值、数据状态、记录是否存在。
  **禁止**把断言断在：页面元素、UI 文案提示、按钮状态、列表展示、页面跳转等前端产物上（接口层根本断不了这些）。
- **纯前端动作**（页面跳转、刷新、按钮置灰、Toast 提示、滚动等）**不产出为测试步骤**，直接忽略。
- 文档没写接口字段名时，用业务语言描述要验证的"事实"即可（如"返回的可用余额应减少100"），仍不要写 path/JSON/SQL。

### 正反例（按右列对齐）

| ❌ 前端视角（不要这样写） | ✅ 接口视角（这样写） |
|---|---|
| 点击"提交换汇"按钮 | 提交换汇申请 |
| 页面显示可用余额 100 | 余额查询返回的可用余额为 100 |
| 提示"提交成功" | 提交接口返回成功（返回码正常 / 申请单号非空） |
| 列表中出现这条换汇记录 | 申请单查询返回的列表包含该条记录 |
| 跳转到申请详情页 | （纯前端跳转，不作为接口测试步骤，忽略） |
| 提交按钮变灰不可重复点 | 重复提交时接口应幂等（返回同一申请单 / 拒绝重复） |
`;
